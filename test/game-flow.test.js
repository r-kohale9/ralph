'use strict';

// ─────────────────────────────────────────────────────────────────────────────
// Tests for the results-screen viewport coverage assertion (TE-RES-001)
//
// The P0 pattern: results screen renders with position:static instead of
// position:fixed, causing CDN body flex-row layout to push #results-screen
// off-viewport (observed: rectTop=144.5px, width=208px across multiple builds).
//
// GEN-UX-001 shipped to gen prompts (position:fixed; inset:0; z-index:100)
// but the T1 static validator cannot catch this — computed styles require
// browser rendering. These tests validate the assertion logic by simulating
// the page.evaluate() return shape and verifying pass/fail conditions.
//
// The actual checkResultsScreenViewport() helper is injected into the shared
// Playwright boilerplate in lib/pipeline-test-gen.js and runs inside every
// game-flow spec file generated by the pipeline.
// ─────────────────────────────────────────────────────────────────────────────

const { describe, it } = require('node:test');
const assert = require('node:assert/strict');

// ─── Replicate the assertion logic from checkResultsScreenViewport() ─────────
//
// This mirrors the page.evaluate() return shape and the assertion condition
// exactly as defined in the boilerplate in lib/pipeline-test-gen.js.
// Any change to one must be reflected in the other.

function assertResultsViewportCoverage(coverageCheck) {
  if (!coverageCheck.found) {
    // Results element not found — skip (game may not have a dedicated results element)
    return { skipped: true };
  }
  if (coverageCheck.skipped) {
    // Element exists but is not visible yet (display:none or zero-size) — skip assertion
    return { skipped: true };
  }
  // TE-RES-002: coversViewport now requires rectTop <= 10 (rectTop guard).
  // A position:relative element may have height >= innerHeight but be scrolled below the fold
  // (rectTop=200px) — the button at the bottom would be off-screen. The guard prevents that
  // false-pass. Mirrors the coversViewport computation in lib/pipeline-test-gen.js page.evaluate().
  const passes = coverageCheck.position === 'fixed' || coverageCheck.coversViewport;
  return {
    skipped: false,
    passes,
    message: passes
      ? null
      : `Results screen must be position:fixed or cover full viewport. Got: position=${coverageCheck.position}, top=${coverageCheck.rectTop}px, height=${coverageCheck.rectHeight}px, coversViewport=${coverageCheck.coversViewport}`,
  };
}

// ─── page.evaluate() return shape validator ───────────────────────────────────
//
// Validates the shape of what page.evaluate() returns. In real tests, this
// runs inside the browser via Playwright. Here we construct synthetic payloads
// to confirm the assertion logic classifies them correctly.

describe('TE-RES-001: results-screen viewport coverage — assertion logic', () => {
  // PASS cases: position:fixed is always acceptable regardless of rect
  it('passes when position is fixed (correct implementation)', () => {
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'fixed',
      zIndex: '100',
      rectTop: 0,
      rectHeight: 844,
      coversViewport: true,
    });
    assert.equal(result.skipped, false);
    assert.equal(result.passes, true);
    assert.equal(result.message, null);
  });

  it('passes when position is fixed even if rectTop is non-zero (off-screen fixed elements)', () => {
    // Some games use position:fixed but hide results off-screen initially
    // until needed. position:fixed itself satisfies the contract.
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'fixed',
      zIndex: '9999',
      rectTop: -1000,
      rectHeight: 844,
      coversViewport: false,
    });
    assert.equal(result.passes, true, 'position:fixed always passes regardless of rectTop');
  });

  it('passes when coversViewport is true even if position is not fixed (absolute-fill pattern)', () => {
    // Some games use position:absolute with 100vh height and top:0 — this also covers
    // the viewport and is acceptable (though position:fixed is preferred).
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'absolute',
      zIndex: '100',
      rectTop: 0,
      rectHeight: 844,
      coversViewport: true,
    });
    assert.equal(result.passes, true, 'coversViewport:true passes even without position:fixed');
  });

  // FAIL cases: the P0 pattern — position:static pushed off-viewport
  it('fails when position is static and screen is off-viewport (P0 bug pattern)', () => {
    // Exact values observed in quadratic-formula #546 and soh-cah-toa #544 audits
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'static',
      zIndex: 'auto',
      rectTop: 145, // Math.round(144.5)
      rectHeight: 300,
      coversViewport: false,
    });
    assert.equal(result.skipped, false);
    assert.equal(result.passes, false);
    assert.ok(result.message.includes('position=static'), 'message shows position value');
    assert.ok(result.message.includes('top=145px'), 'message shows rectTop');
    assert.ok(result.message.includes('coversViewport=false'), 'message shows coversViewport');
  });

  it('fails when position is relative and screen does not cover viewport', () => {
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'relative',
      zIndex: '1',
      rectTop: 500,
      rectHeight: 200,
      coversViewport: false,
    });
    assert.equal(result.passes, false);
    assert.ok(result.message.includes('position=relative'));
    assert.ok(result.message.includes('top=500px'));
  });

  it('fails when position is sticky and screen does not cover viewport', () => {
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'sticky',
      zIndex: '10',
      rectTop: 200,
      rectHeight: 400,
      coversViewport: false,
    });
    assert.equal(result.passes, false);
    assert.ok(result.message.includes('position=sticky'));
  });

  // SKIP case: no results element found
  it('skips when results element is not found (game has no dedicated results element)', () => {
    const result = assertResultsViewportCoverage({ found: false });
    assert.equal(result.skipped, true);
    assert.equal(result.passes, undefined, 'no pass/fail when skipped');
  });

  // TE-RES-002: rectTop guard — false-pass prevention for scrolled-down relative screens
  it('fails when height >= innerHeight but rectTop=200px (scrolled below fold — coversViewport=false)', () => {
    // A position:relative element in a full-height flex parent may have height >= innerHeight
    // but be pushed 200px below the viewport origin. Without the rectTop guard, an older
    // coversViewport formula could incorrectly pass if it only checked height. Verifies the
    // guard is enforced: coversViewport must be false when rectTop=200 > 10.
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'relative',
      zIndex: 'auto',
      rectTop: 200,
      rectHeight: 667,
      coversViewport: false, // rectTop=200 > 10 — coversViewport correctly false
    });
    assert.equal(result.skipped, false);
    assert.equal(result.passes, false, 'rectTop=200 must fail even when height covers viewport');
    assert.ok(result.message.includes('top=200px'), 'message shows scrolled-down rectTop');
  });

  it('passes when position:fixed and top=0 (standard fully-correct implementation)', () => {
    // Canonical case: gen prompt shipped position:fixed; inset:0; z-index:100
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'fixed',
      zIndex: '100',
      rectTop: 0,
      rectHeight: 844,
      coversViewport: true,
    });
    assert.equal(result.skipped, false);
    assert.equal(result.passes, true, 'position:fixed with top=0 must pass');
    assert.equal(result.message, null);
  });

  it('passes when height >= innerHeight AND rectTop <= 10 (full-viewport relative with rectTop guard satisfied)', () => {
    // position:absolute/relative with 100vh height and top within 10px tolerance is acceptable
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'absolute',
      zIndex: '50',
      rectTop: 5,
      rectHeight: 844,
      coversViewport: true, // rectTop=5 <= 10 AND height=844 >= innerHeight
    });
    assert.equal(result.skipped, false);
    assert.equal(result.passes, true, 'height >= innerHeight with rectTop=5 within tolerance must pass');
    assert.equal(result.message, null);
  });

  // CR-019: SKIP cases — element exists but not yet visible (display:none or zero-size)
  it('skips when element exists but display:none (results screen hidden before game ends)', () => {
    // page.evaluate() returns skipped:true when getComputedStyle(el).display === 'none'
    // This prevents false-positive assertion failure before the game reaches results phase
    const result = assertResultsViewportCoverage({ found: true, skipped: true, reason: 'element not visible yet' });
    assert.equal(result.skipped, true);
    assert.equal(result.passes, undefined, 'no pass/fail when element is display:none');
  });

  it('skips when element exists but has zero height AND zero width (not yet rendered)', () => {
    // page.evaluate() returns skipped:true when rect.height === 0 && rect.width === 0
    // Covers elements that exist in DOM but have not yet been shown (e.g. opacity:0 with no layout)
    const result = assertResultsViewportCoverage({ found: true, skipped: true, reason: 'element not visible yet' });
    assert.equal(result.skipped, true);
    assert.equal(result.passes, undefined, 'no pass/fail when element is zero-size');
  });

  // CR-019: FAIL case preserved — visible element with position:static still fails
  it('still fails when element is visible (display:block) but position:static off-viewport', () => {
    // When element IS visible (not skipped), existing assertion logic must still fire
    const result = assertResultsViewportCoverage({
      found: true,
      position: 'static',
      zIndex: 'auto',
      rectTop: 145,
      rectHeight: 300,
      coversViewport: false,
    });
    assert.equal(result.skipped, false);
    assert.equal(result.passes, false, 'visible position:static element still triggers failure');
    assert.ok(result.message.includes('position=static'), 'message includes position');
  });
});

// ─── coversViewport computation logic ────────────────────────────────────────
//
// These tests validate the coversViewport flag logic as it would be computed
// inside page.evaluate() in the browser.
// TE-RES-002: flag is: rect.top <= 10 && rect.height >= window.innerHeight
// The <= 10 guard (was <= 0) prevents false-pass when a position:relative
// element has height >= innerHeight but is scrolled below the fold (rectTop=200px).

describe('TE-RES-001: coversViewport flag computation', () => {
  function computeCoversViewport(rectTop, rectHeight, viewportHeight) {
    // Mirrors the page.evaluate() logic in lib/pipeline-test-gen.js (TE-RES-002)
    return rectTop <= 10 && rectHeight >= viewportHeight;
  }

  it('coversViewport is true when top=0 and height equals viewport height (iPhone SE: 667px)', () => {
    assert.equal(computeCoversViewport(0, 667, 667), true);
  });

  it('coversViewport is true when top=0 and height exceeds viewport height', () => {
    assert.equal(computeCoversViewport(0, 844, 667), true);
  });

  it('coversViewport is false when top > 0 (screen pushed down by flex-row parent)', () => {
    // The exact CDN body flex-row push observed in UI/UX audits
    assert.equal(computeCoversViewport(145, 667, 667), false);
  });

  it('coversViewport is false when height < viewportHeight (partial coverage)', () => {
    assert.equal(computeCoversViewport(0, 300, 667), false);
  });

  it('coversViewport is true when top is slightly negative (fixed element with top:-1px for hairline)', () => {
    assert.equal(computeCoversViewport(-1, 668, 667), true);
  });

  // TE-RES-002: rectTop guard boundary tests
  it('coversViewport is true when top=10 and height covers viewport (10px tolerance boundary)', () => {
    // Allows up to 10px of top offset — e.g. a 1px border or sub-pixel rounding
    assert.equal(computeCoversViewport(10, 667, 667), true);
  });

  it('coversViewport is false when top=11 and height covers viewport (just outside tolerance)', () => {
    // 11px > 10px tolerance — screen starts too far down, bottom of results would be off-fold
    assert.equal(computeCoversViewport(11, 667, 667), false);
  });
});

